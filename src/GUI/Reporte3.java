    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = "";
        nombre = JOptionPane.showInputDialog("Ingrese el titulo a buscar");
        if (nombre != null) {
            LinkedList datos = new LinkedList();
            datos = consultaDAO.consulta3(nombre);
            if (!datos.isEmpty()) {
                for (int i = 0; i < datos.size(); i++) {
                    libroVO elreporte = (libroVO) datos.get(i);
                    for (int y = modeloreporte3.getRowCount() - 1; y >= 0; y--) {
                        modeloreporte3.removeRow(y);
                    }
                    for (int h = 0; h < datos.size(); h++) {
                        libroVO p1 = (libroVO) datos.get(h);
                        modeloreporte3.insertRow(h, new Object[]{});
                        modeloreporte3.setValueAt(p1.getid(), h, 0);
                        modeloreporte3.setValueAt(p1.gettitulo(), h, 1);
                        modeloreporte3.setValueAt(p1.getarea(), h, 3);
                        modeloreporte3.setValueAt(p1.geteditorial(), h, 2);
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Haz cancelado la operacion :V");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

