    public void imprimir() {
        LinkedList datos = new LinkedList();
        datos = consultaDAO.imprimir();
        if (!datos.isEmpty()) {
            for (int i = 0; i < datos.size(); i++) {
                libroVO loslibros = (libroVO) datos.get(i);
                for (int y = modelolibros.getRowCount() - 1; y >= 0; y--) {
                    modelolibros.removeRow(y);
                }
                for (int h = 0; h < datos.size(); h++) {
                    libroVO p1 = (libroVO) datos.get(h);
                    modelolibros.insertRow(h, new Object[]{});
                    modelolibros.setValueAt(p1.getid(), h, 0);
                    modelolibros.setValueAt(p1.gettitulo(), h, 1);
                    modelolibros.setValueAt(p1.geteditorial(), h, 2);
                    modelolibros.setValueAt(p1.getarea(), h, 3);
                }
            }
        }
    }

