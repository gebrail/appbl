    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (idlibro.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Falta Id");
            idlibro.requestFocus();
        } else if (titulolibro.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Falta el titulo");
            titulolibro.requestFocus();
        } else if (editorialibro.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Falta la editorial");
            editorialibro.requestFocus();
        } else if (arealibro.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Falta el area");
            arealibro.requestFocus();
        } else {
            int id = Integer.parseInt(idlibro.getText());
            consultaDAO.registrarlibro(id, titulolibro.getText(), editorialibro.getText(), arealibro.getText());
            imprimir();
            JOptionPane.showMessageDialog(rootPane, "El Libro ha sido Guardado");
            idlibro.setText("");
            titulolibro.setText("");
            editorialibro.setText("");
            arealibro.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

